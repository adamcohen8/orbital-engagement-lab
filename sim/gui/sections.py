    owner.orbit_adaptive_atol_spin.setValue(1.0e-9)
    owner.orbit_adaptive_rtol_spin = owner._make_free_spinbox(decimals=12)
    owner.orbit_adaptive_rtol_spin.setRange(0.0, 1.0)
    owner.orbit_adaptive_rtol_spin.setValue(1.0e-7)
    owner.output_mode_combo = QComboBox()
    owner.output_mode_combo.addItems(owner.output_modes)
    owner.output_dir_edit = QLineEdit()
    owner.output_dir_edit.setMinimumWidth(320)
    owner.output_dir_browse_button = QPushButton("Browse...")
    owner.output_dir_browse_button.clicked.connect(owner._browse_output_directory)
    owner.orbit_substep_enabled_check = QCheckBox("Orbit Substep")
    owner.orbit_substep_spin = owner._make_free_spinbox()
    owner.attitude_substep_enabled_check = QCheckBox("Attitude Substep")
    owner.attitude_substep_spin = owner._make_free_spinbox()
