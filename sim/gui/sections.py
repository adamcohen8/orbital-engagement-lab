    output_dir_layout = QHBoxLayout(output_dir_row)
    output_dir_layout.setContentsMargins(0, 0, 0, 0)
    output_dir_layout.setSpacing(6)
    output_dir_layout.addWidget(owner.output_dir_edit, 1)
    output_dir_layout.addWidget(owner.output_dir_browse_button)
    layout.addRow("Output Directory", output_dir_row)
    layout.addRow("Adaptive Abs Tol", owner.orbit_adaptive_atol_spin)
    layout.addRow("Adaptive Rel Tol", owner.orbit_adaptive_rtol_spin)
