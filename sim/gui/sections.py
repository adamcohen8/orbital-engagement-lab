    owner.dt_spin.setRange(0.000001, 1.0e6)
    owner.dt_spin.setDecimals(6)
    owner.dt_spin.setValue(1.0)
    owner.orbit_integrator_combo = QComboBox()
    owner.orbit_integrator_combo.addItems(owner.orbit_integrator_options)
